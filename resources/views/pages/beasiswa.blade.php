                            <h3><strong>BEASISWA PPA (Peningkatan Prestasi Akademik)</strong></h3>



                            <h5>A. Gambaran Umum</h5>



                            <p>Undang-undang
                                Nomor 20 Tahun 2003 tentang Sistem Pendidikan Nasional, Bab V pasal 12 (1.c),
                                menyebutkan bahwa setiap peserta didik pada setiap satuan pendidikan berhak
                                mendapatkan beasiswa bagi yang berprestasi yang orang tuanya tidak mampu
                                membiayai pendidikannya. Pasal 12 (1.d), menyebutkan bahwa setiap peserta didik
                                pada setiap satuan pendidikan berhak mendapatkan biaya pendidikan bagi mereka
                                yang orang tuanya tidak mampu membiayai pendidikannya.Selain itu di dalamUndang-undang
                                Nomor 12 Tahun 2012 tentang Pendidikan Tinggidi dalam Pasal 76 Ayat (2) juga
                                jelas mengamanahkan tentang pemenuhan hak mahasiswa yaitu pemerintah harus
                                memberikan: (a) beasiswa kepada Mahasiswa berprestasi; (b) bantuan atau
                                membebaskan biaya Pendidikan; dan/atau (c)pinjaman dana tanpa bunga yang wajib
                                dilunasi setelah lulus dan/atau memperoleh pekerjaan.</p>



                            <p>Dijelaskan
                                lebih lanjut di dalam penjelasan, yang dimaksud dengan “beasiswa” adalah
                                dukungan biaya Pendidikanyang diberikan kepada Mahasiswa untuk mengikuti
                                dan/atau menyelesaikan Pendidikan Tinggi berdasarkan pertimbangan utama
                                prestasi dan/atau potensi akademik. Sedangkan “bantuan biaya pendidikan” adalah
                                dukungan biaya Pendidikan yang diberikan kepada Mahasiswa untuk mengikuti
                                dan/atau menyelesaikan Pendidikan Tinggi berdasarkan pertimbangan utama
                                keterbatasan kemampuan ekonomi.</p>



                            <p>Peraturan
                                Pemerintah Nomor 48 tahun 2008 tentang Pendanaan Pendidikan, Bagian Kelima,
                                Pasal 27 ayat (1), menyebutkan bahwa Pemerintahdan pemerintah daerah sesuai
                                kewenangannya memberi bantuan biaya pendidikan atau beasiswa kepada peserta
                                didik yang orang tua atau walinya tidak mampu membiayai pendidikannya. Pasal 27
                                ayat (2), menyebutkan bahwa Pemerintah dan pemerintah daerah sesuai dengan
                                kewenangannya dapat memberi beasiswa kepada peserta didik yang berprestasi.</p>



                            <p>Mengacu
                                kepada Undang-undang dan Peraturan Pemerintah tersebut, maka Pemerintah melalui
                                Direktorat Jenderal Pembelajaran dan Kemahasiswaan, mengupayakan pemberian
                                beasiswa bagi mahasiswa yang berprestasi.</p>



